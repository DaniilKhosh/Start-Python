print("Введите число от 100 до 999")
a = int(input("a="))
if a>100 and a <999:
   a1 = a // 100
   a2 = a % 100 // 10
   a3 = a % 100 % 10
   print("Сумма чисел равна :", a1 + a2 + a3)
   print("Произведение чисел равно :", a1 * a2 * a3)
else:
   print("Введено некорректное число. Попробуйте заново")